<h1 class="mb-4">À propos d’EcoRide</h1>

<p class="lead">
    EcoRide est une plateforme de covoiturage pensée pour être simple,
    économique et responsable.
</p>

<p>
    L’objectif est de mettre en relation conducteurs et passagers autour
    de trajets clairs, transparents et accessibles à tous, tout en réduisant
    l’impact environnemental des déplacements individuels.
</p>

<p>
    Chaque trajet partagé est une action concrète en faveur d’une mobilité
    plus durable.
</p>
