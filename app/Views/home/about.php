<h1 class="mb-4">À propos d’EcoRide</h1>

<p class="lead">
    EcoRide est une plateforme de covoiturage conçue pour faciliter le partage de trajets
    entre conducteurs et passagers de manière simple, transparente et responsable.
</p>

<section class="mb-4">
    <h2>Notre objectif</h2>
    <p>
        EcoRide a pour objectif de proposer une solution de mobilité collaborative
        permettant de réduire le nombre de véhicules en circulation tout en facilitant
        les déplacements quotidiens.
    </p>
    <p>
        En partageant un trajet, les utilisateurs contribuent à diminuer les coûts
        de transport et à limiter l’impact environnemental lié aux déplacements
        individuels.
    </p>
</section>

<section class="mb-4">
    <h2>Comment fonctionne la plateforme</h2>
    <p>
        La plateforme permet aux conducteurs de publier un trajet avec les informations
        essentielles : lieu de départ, destination, date, nombre de places disponibles
        et prix du trajet.
    </p>
    <p>
        Les passagers peuvent rechercher un trajet correspondant à leurs besoins,
        réserver une place et suivre l’historique de leurs déplacements.
    </p>
</section>

<section class="mb-4">
    <h2>Un système simple et transparent</h2>
    <p>
        EcoRide utilise un système de crédits permettant de simplifier les transactions
        entre utilisateurs. Chaque réservation correspond à un nombre de crédits défini
        lors de la publication du trajet.
    </p>
    <p>
        Une fois le trajet terminé, les passagers peuvent confirmer le bon déroulement
        du trajet et laisser un avis afin de garantir la qualité des échanges sur
        la plateforme.
    </p>
</section>

<section>
    <h2>Un projet pédagogique</h2>
    <p>
        EcoRide est un projet réalisé dans le cadre d’une formation au développement
        web. Il illustre la conception d’une application web complète intégrant
        gestion des utilisateurs, publication de trajets, réservations, avis,
        gestion d’incidents et interface d’administration.
    </p>
</section>