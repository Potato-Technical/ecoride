<h1 class="mb-4">Accessibilité</h1>

<section class="mb-4">
    <h2>1. Engagement</h2>
    <p>
        EcoRide accorde une attention particulière à l’accessibilité de son interface afin de proposer
        une navigation claire, lisible et utilisable sur différents supports.
    </p>
    <p>
        Le projet est conçu dans une logique de simplicité d’usage, avec une attention portée à la lisibilité
        des contenus, à la cohérence des parcours et à la compréhension des actions principales.
    </p>
</section>

<section class="mb-4">
    <h2>2. Principes mis en œuvre</h2>
    <p>
        Dans le cadre de ce projet, plusieurs choix ont été appliqués pour améliorer l’accessibilité
        de l’interface :
    </p>
    <ul>
        <li>structure de pages claire et hiérarchisée ;</li>
        <li>titres et contenus organisés de manière lisible ;</li>
        <li>contrastes visuels travaillés pour faciliter la lecture ;</li>
        <li>navigation responsive adaptée aux usages mobiles et desktop ;</li>
        <li>libellés explicites sur les formulaires et les actions principales ;</li>
        <li>prise en compte des états d’erreur, de validation et de retour utilisateur.</li>
    </ul>
</section>

<section class="mb-4">
    <h2>3. Limites actuelles</h2>
    <p>
        EcoRide est un projet pédagogique en évolution. Malgré le soin apporté à la conception de l’interface,
        certains éléments peuvent encore présenter des limites d’accessibilité ou nécessiter des améliorations.
    </p>
    <p>
        L’objectif est d’améliorer progressivement l’expérience pour tous les utilisateurs, notamment sur les
        parcours critiques comme la recherche de trajets, la réservation, la connexion et la gestion de compte.
    </p>
</section>

<section class="mb-4">
    <h2>4. Améliorations visées</h2>
    <p>
        Les axes d’amélioration envisagés concernent notamment :
    </p>
    <ul>
        <li>le renforcement de la navigation clavier ;</li>
        <li>l’amélioration des aides visuelles sur certains composants ;</li>
        <li>l’enrichissement des retours d’état pour les formulaires ;</li>
        <li>la vérification plus systématique de l’accessibilité des composants réutilisables.</li>
    </ul>
</section>

<section>
    <h2>5. Contact</h2>
